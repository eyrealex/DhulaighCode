public class MathsCal {
	

	public static int divideMethod(int num1, int num2) {
		return num1/num2;
	}//end divideMethod

	public static int multiplyMthod(int num1, int num2) {
		return num1*num2;
	}//end multiplyMethod

	public static int subtractMethod(int num1, int num2) {
		return num1-num2;
	}//end subtractMethod

	public static int addMethod(int num1, int num2) {
		return num1+num2;
	}//end addMethod

}//end MathsCal class
